def add(nbr1,nbr2)
  return nbr1 + nbr2
end

def subtract(data1,data2)
  return data1 - data2
end

def sum(nbr)
  return nbr.sum
end

def multiply(input1,input2)
  return input1*input2
end

def power(a1,a2)
  return a1**a2
end

def factorial(ft)
  f = 1; for i in 1..ft ; f *= i; end;
  return f
end
